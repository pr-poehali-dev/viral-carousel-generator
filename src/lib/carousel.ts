const CODE_WORDS = ['СТАРТ', 'ГАЙД', 'ХОЧУ', 'ОГОНЬ', 'ДАЙ', 'ТОП', 'РОСТ', 'БОНУС'];

const pick = <T,>(arr: T[]) => arr[Math.floor(Math.random() * arr.length)];

export function generateCarousel(topic: string, style: Style, count = 7): Carousel {
  const t = topic.trim() || 'твоя тема';
  const hooks = HOOKS[style](t);
  const texts = SLIDE_TEXTS[style](t);
  const hook = pick(hooks);
  const codeWord = pick(CODE_WORDS);
  const cta = pick(CTAS[style]);
  const visualBase = pick(VISUAL_THEMES);

  const n = Math.max(3, Math.min(10, count));
  const slides: Slide[] = Array.from({ length: n }).map((_, i) => {
    const role = SLIDE_ROLES[i] ?? `Слайд ${i + 1}`;
    const text = i === 0 ? hook : (texts[i] ?? texts[texts.length - 1]);
    const isLast = i === n - 1;
    const visualIdea =
      i === 0
        ? 'Яркая обложка с крупным заголовком и интригующим акцентом'
        : isLast
        ? 'Финальный слайд с призывом к действию и кодовым словом'
        : `Иллюстрация под смысл слайда «${role.toLowerCase()}»`;
    const prompt = `${visualBase}. Слайд ${i + 1} карусели Instagram про «${t}», смысл: ${role.toLowerCase()}. Формат 4:5, место для текста сверху, единый фирменный стиль, высокая детализация, тренд 2025 --ar 4:5 --style raw`;
    return { index: i, role, text, visualIdea, prompt };
  });

  return {
    id: Math.random().toString(36).slice(2),
    topic: t,
    style,
    createdAt: Date.now(),
    hook,
    slides,
    cta,
    codeWord,
  };
}

